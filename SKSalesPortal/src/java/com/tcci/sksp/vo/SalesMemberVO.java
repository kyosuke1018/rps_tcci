package com.tcci.sksp.vo;

import com.google.gson.annotations.Expose;
import com.tcci.sksp.entity.org.SkSalesMember;

/**
 *
 * @author Neo.Fu
 */
public class SalesMemberVO {
    @Expose
    SkSalesMember salesMember;
    @Expose
    String areaCode;
    @Expose
    String areaName;

    public SkSalesMember getSalesMember() {
        return salesMember;
    }

    public void setSalesMember(SkSalesMember salesMember) {
        this.salesMember = salesMember;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }

    public String getAreaName() {
        return areaName;
    }

    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }
}
