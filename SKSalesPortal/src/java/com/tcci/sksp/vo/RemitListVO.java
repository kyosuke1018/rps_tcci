package com.tcci.sksp.vo;

import java.util.List;

/**
 *
 * @author Lynn.Huang
 */
public class RemitListVO {
    private List<RemitMasterVO> remitMasterList;
    private List<CashSummaryVO> cashSummaryList;
    private List<CheckSummaryVO> checkSummaryList;

    public List<CashSummaryVO> getCashSummaryList() {
        return cashSummaryList;
    }

    public void setCashSummaryList(List<CashSummaryVO> cashSummaryList) {
        this.cashSummaryList = cashSummaryList;
    }

    public List<CheckSummaryVO> getCheckSummaryList() {
        return checkSummaryList;
    }

    public void setCheckSummaryList(List<CheckSummaryVO> checkSummaryList) {
        this.checkSummaryList = checkSummaryList;
    }

    public List<RemitMasterVO> getRemitMasterList() {
        return remitMasterList;
    }

    public void setRemitMasterList(List<RemitMasterVO> remitMasterList) {
        this.remitMasterList = remitMasterList;
    }

}
