package com.tcci.fc.controller.bpm;

import com.tcci.fc.entity.org.TcUser;
import java.util.Date;

/**
 *
 * @author Greg.Chou
 */
public class HumanTaskLog {
    private String taskName;
    private TcUser owner;
    private TcUser creator;
    private String action;
    private String comment;
    private Date createDatetime;
    private Date processDatetime;
    private WorkflowStatus status;

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public TcUser getOwner() {
        return owner;
    }

    public void setOwner(TcUser owner) {
        this.owner = owner;
    }

    public Date getProcessDatetime() {
        return processDatetime;
    }

    public void setProcessDatetime(Date processDatetime) {
        this.processDatetime = processDatetime;
    }

    public WorkflowStatus getStatus() {
        return status;
    }

    public void setStatus(WorkflowStatus status) {
        this.status = status;
    }

    public String getTaskName() {
        return taskName;
    }

    public void setTaskName(String taskName) {
        this.taskName = taskName;
    }

    public Date getCreateDatetime() {
        return createDatetime;
    }

    public void setCreateDatetime(Date createDatetime) {
        this.createDatetime = createDatetime;
    }

    public TcUser getCreator() {
        return creator;
    }

    public void setCreator(TcUser creator) {
        this.creator = creator;
    }
}
