package com.tcci.ec.model;

import java.io.Serializable;

/**
 *
 * @author Peter.pan
 */
public class ServiceVO implements Serializable {
    private static final long serialVersionUID = 1L;    
    private Long id;
    private String service;
    private String serviceUrl;
    private String description;
    private Boolean active;

    public ServiceVO() {
    }

    public ServiceVO(Long id) {
        this.id = id;
    }

    public ServiceVO(Long id, String service, String serviceUrl, Boolean active) {
        this.id = id;
        this.service = service;
        this.serviceUrl = serviceUrl;
        this.active = active;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getService() {
        return service;
    }

    public void setService(String service) {
        this.service = service;
    }

    public String getServiceUrl() {
        return serviceUrl;
    }

    public void setServiceUrl(String serviceUrl) {
        this.serviceUrl = serviceUrl;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Boolean getActive() {
        return active;
    }

    public void setActive(Boolean active) {
        this.active = active;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof ServiceVO)) {
            return false;
        }
        ServiceVO other = (ServiceVO) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.tcci.ec.model.ServiceVO[ id=" + id + " ]";
    }
    
}
