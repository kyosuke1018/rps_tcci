package com.tcci.ec.enums;

import com.tcci.cm.model.global.GlobalConstant;
import com.tcci.fc.util.ResourceBundleUtils;
import java.util.Locale;

/**
 *
 * @author Peter.pan
 */
public enum SmsContentEnum {
    REGISTRY("R", "【TCC-EC】台泥電商新會員註冊驗證碼通知，您的驗證碼為 [{0}] 五碼數字。"),
    RESET_PASSWORD("P", "【TCC-EC】忘記密碼通知，您的密碼已重設為 [{0}] 六碼字元，您可登入系統再自行修改。")
    ;
    
    private String code;
    private String name;
    
    SmsContentEnum(String code, String name){
        this.code = code;
        this.name = name;
    }
    
    public static SmsContentEnum getFromCode(String code){
        for (SmsContentEnum enum1 : SmsContentEnum.values()) {
            if( code!=null && enum1.getCode().equals(code) ) {
                return enum1;
            }
        }
        return null; // default
    }
    
    /**
     * 顯示名稱 (取自enum.properties => [class name].[enum name])
     * @return 
     */
    public String getDisplayName(){
        String res = ResourceBundleUtils.getDisplayName(GlobalConstant.ENUM_RESOURCE , this.getClass().getSimpleName()+"."+this.toString() );
        if( res==null ){
            res = name;
        }
        return res;
    } 
    public String getDisplayName(Locale locale){
        String res = ResourceBundleUtils.getDisplayName(locale, GlobalConstant.ENUM_RESOURCE , this.getClass().getSimpleName()+"."+this.toString() );
        if( res==null ){
            res = name;
        }
        return res;
    }
    
    //<editor-fold defaultstate="collapsed" desc="getter and setter">
    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    //</editor-fold>
}

