package com.tcci.storeadmin.facade.sync.contract;

import com.tcci.storeadmin.facade.sync.SyncBaseVO;
import com.tcci.tccstore.entity.EcContract;
import com.tcci.tccstore.entity.EcContractProduct;
import com.tcci.tccstore.entity.EcPlant;
import com.tcci.tccstore.entity.EcProduct;
import com.tcci.tccstore.entity.EcSalesarea;

/**
 *
 * @author Jimmy.Lee
 */
public class ContractProductVO extends SyncBaseVO {

    private String contract;
    private int posnr;
    private String product;
    private String plant;
    private String salesarea;
    private String method;

    private EcContractProduct entity;
    private EcContract ecContract;
    private EcProduct ecProduct;
    private EcPlant ecPlant;
    private EcSalesarea ecSalesarea;

    // c'tor
    public ContractProductVO() {
    }
    
    public ContractProductVO(String contract, int posnr) {
        this.contract = contract;
        this.posnr = posnr;
    }
    
    // getter, setter
    public String getContract() {
        return contract;
    }

    public void setContract(String contract) {
        this.contract = contract;
    }

    public int getPosnr() {
        return posnr;
    }

    public void setPosnr(int posnr) {
        this.posnr = posnr;
    }

    public String getProduct() {
        return product;
    }

    public void setProduct(String product) {
        this.product = product;
    }

    public String getPlant() {
        return plant;
    }

    public void setPlant(String plant) {
        this.plant = plant;
    }

    public String getSalesarea() {
        return salesarea;
    }

    public void setSalesarea(String salesarea) {
        this.salesarea = salesarea;
    }

    public String getMethod() {
        return method;
    }

    public void setMethod(String method) {
        this.method = method;
    }

    public EcContractProduct getEntity() {
        return entity;
    }

    public void setEntity(EcContractProduct entity) {
        this.entity = entity;
    }

    public EcContract getEcContract() {
        return ecContract;
    }

    public void setEcContract(EcContract ecContract) {
        this.ecContract = ecContract;
    }

    public EcProduct getEcProduct() {
        return ecProduct;
    }

    public void setEcProduct(EcProduct ecProduct) {
        this.ecProduct = ecProduct;
    }

    public EcPlant getEcPlant() {
        return ecPlant;
    }

    public void setEcPlant(EcPlant ecPlant) {
        this.ecPlant = ecPlant;
    }

    public EcSalesarea getEcSalesarea() {
        return ecSalesarea;
    }

    public void setEcSalesarea(EcSalesarea ecSalesarea) {
        this.ecSalesarea = ecSalesarea;
    }

}
