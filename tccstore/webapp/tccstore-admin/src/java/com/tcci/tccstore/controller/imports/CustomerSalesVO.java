package com.tcci.tccstore.controller.imports;

import com.tcci.fc.controller.dataimport.ExcelVOBase;
import com.tcci.tccstore.entity.EcCustomer;
import com.tcci.tccstore.entity.EcCustomerSales;
import com.tcci.tccstore.entity.EcSales;
import java.util.List;

/**
 *
 * @author Jimmy.Lee
 */
public class CustomerSalesVO extends ExcelVOBase {

    private String customer;
    private String sales;
    private String salesList;

    private EcCustomerSales entity;
    private EcCustomer ecCustomer;
    private EcSales ecSales;
    private List<EcSales> ecSalesList;
    
    public EcCustomerSales getEntity() {
        return entity;
    }

    // getter, setter
    public void setEntity(EcCustomerSales entity) {    
        this.entity = entity;
    }

    public String getCustomer() {
        return customer;
    }

    public void setCustomer(String customer) {
        this.customer = customer;
    }

    public String getSales() {
        return sales;
    }

    public void setSales(String sales) {
        this.sales = sales;
    }

    public EcCustomer getEcCustomer() {
        return ecCustomer;
    }

    public void setEcCustomer(EcCustomer ecCustomer) {
        this.ecCustomer = ecCustomer;
    }

    public EcSales getEcSales() {
        return ecSales;
    }

    public void setEcSales(EcSales ecSales) {
        this.ecSales = ecSales;
    }

    public String getSalesList() {
        return salesList;
    }

    public void setSalesList(String salesList) {
        this.salesList = salesList;
    }

    public List<EcSales> getEcSalesList() {
        return ecSalesList;
    }

    public void setEcSalesList(List<EcSales> ecSalesList) {
        this.ecSalesList = ecSalesList;
    }

}
