package com.tcci.fcs.model.reprot;

import java.math.BigDecimal;

/**
 *
 * @author Kyle.Cheng
 */
public class ReportBaseVO {
    private String ym;//報表年月
    private String compCode;//公司代碼
    private String compName;//公司名稱
    private String abbreviation;//公司簡稱
    private String accCode;//會科代碼
    private String accName;//會科名稱
    private BigDecimal amount;//金額
    private BigDecimal amountXls;//原幣別金額
    private String comp2Code;//對象公司代碼(D0206:支出;D0208:收入)
    private String comp2Name;//對象公司名稱(D0206:支出;D0208:收入)
    //合併營收上傳 A0102
    private String currCode;//原幣別代碼
    private BigDecimal amountMonthly;//當月差異 金額
    private BigDecimal amountXlsMonthly;//當月差異 原幣別金額
    
    private String sheetCode;

    public String getYm() {
        return ym;
    }

    public void setYm(String ym) {
        this.ym = ym;
    }

    public String getCompCode() {
        return compCode;
    }

    public void setCompCode(String compCode) {
        this.compCode = compCode;
    }

    public String getCompName() {
        return compName;
    }

    public void setCompName(String compName) {
        this.compName = compName;
    }

    public String getAbbreviation() {
        return abbreviation;
    }

    public void setAbbreviation(String abbreviation) {
        this.abbreviation = abbreviation;
    }
    
    public String getAccCode() {
        return accCode;
    }

    public void setAccCode(String accCode) {
        this.accCode = accCode;
    }

    public String getAccName() {
        return accName;
    }

    public void setAccName(String accName) {
        this.accName = accName;
    }

    public BigDecimal getAmount() {
        return amount;
    }

    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }

    public BigDecimal getAmountXls() {
        return amountXls;
    }

    public void setAmountXls(BigDecimal amountXls) {
        this.amountXls = amountXls;
    }

    public String getComp2Code() {
        return comp2Code;
    }

    public void setComp2Code(String comp2Code) {
        this.comp2Code = comp2Code;
    }

    public String getComp2Name() {
        return comp2Name;
    }

    public void setComp2Name(String comp2Name) {
        this.comp2Name = comp2Name;
    }

    public String getCurrCode() {
        return currCode;
    }

    public void setCurrCode(String currCode) {
        this.currCode = currCode;
    }

    public BigDecimal getAmountMonthly() {
        return amountMonthly;
    }

    public void setAmountMonthly(BigDecimal amountMonthly) {
        this.amountMonthly = amountMonthly;
    }

    public BigDecimal getAmountXlsMonthly() {
        return amountXlsMonthly;
    }

    public void setAmountXlsMonthly(BigDecimal amountXlsMonthly) {
        this.amountXlsMonthly = amountXlsMonthly;
    }

    public String getSheetCode() {
        return sheetCode;
    }

    public void setSheetCode(String sheetCode) {
        this.sheetCode = sheetCode;
    }

}
