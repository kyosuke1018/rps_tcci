package com.tcci.fcs.util;

import java.util.List;

/**
 *
 * @author Jimmy.Lee
 */
public class ExcelValueItem {
    private String sheetCode;
    private String sheetName;
    private int startRow;
    private List<ExcelAccountItem> acItems;

    // c'tor
    public ExcelValueItem () {
    }
    
    public ExcelValueItem (String sheetCode, String sheetName, int startRow, List<ExcelAccountItem> acItems) {
        this.sheetCode = sheetCode;
        this.sheetName = sheetName;
        this.startRow = startRow;
        this.acItems = acItems;
    }
    
    // getter, setter
    public String getSheetCode() {
        return sheetCode;
    }

    public void setSheetCode(String sheetCode) {
        this.sheetCode = sheetCode;
    }

    public String getSheetName() {
        return sheetName;
    }

    public void setSheetName(String sheetName) {
        this.sheetName = sheetName;
    }

    public int getStartRow() {
        return startRow;
    }

    public void setStartRow(int startRow) {
        this.startRow = startRow;
    }

    public List<ExcelAccountItem> getAcItems() {
        return acItems;
    }

    public void setAcItems(List<ExcelAccountItem> acItems) {
        this.acItems = acItems;
    }

}
