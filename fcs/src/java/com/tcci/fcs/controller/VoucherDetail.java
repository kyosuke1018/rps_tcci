package com.tcci.fcs.controller;

import java.math.BigDecimal;

/**
 *
 * @author Mendel.lee
 */
public class VoucherDetail {
    
    private Long Id;
    private Long serial;
    private String debitAndCredit;
    private String account;
    private String accountName;
    private String detail;
    private String detailName;
    private BigDecimal amount;
    private String remark;
    
    public VoucherDetail() {
	
	this.serial = 1L;
	this.debitAndCredit = "D";
	this.amount = new BigDecimal("0");
    }

    public Long getId() {
	return Id;
    }

    public void setId(Long Id) {
	this.Id = Id;
    }

    public Long getSerial() {
	return serial;
    }

    public void setSerial(Long serial) {
	this.serial = serial;
    }

    public String getDebitAndCredit() {
	return debitAndCredit;
    }

    public void setDebitAndCredit(String debitAndCredit) {
	this.debitAndCredit = debitAndCredit;
    }

    public String getAccount() {
	return account;
    }

    public void setAccount(String account) {
	this.account = account;
    }

    public String getAccountName() {
	return accountName;
    }

    public void setAccountName(String accountName) {
	this.accountName = accountName;
    }

    public String getDetail() {
	return detail;
    }

    public void setDetail(String detail) {
	this.detail = detail;
    }

    public String getDetailName() {
	return detailName;
    }

    public void setDetailName(String detailName) {
	this.detailName = detailName;
    }

    public BigDecimal getAmount() {
	return amount;
    }

    public void setAmount(BigDecimal amount) {
	this.amount = amount;
    }

    public String getRemark() {
	return remark;
    }

    public void setRemark(String remark) {
	this.remark = remark;
    }
}
